<h4>Use content filter?</h4>
<p>By default, <strong>WP Models</strong> utilizes the WordPress filter 'the_content' to add the model information, pictures, videos. etc. to the post when displayed to your website visitors. This uses a default template that is included with the plugin. This allows the plugin to cooperate nicely with other plugins, particularly membership plugins that protect content using this filter.</p>
<p>If you have created your own templates for the model post type, please uncheck this setting.</p>
<h4>Flowplayer Settings</h4>
<p><strong>WP Models</strong> uses Flowplayer to display videos attached the model posts. You can specify the player theme used on the frontend here. For a demonstration of the available themes, visit the <a href="http://flowplayer.org/demos" target="_blank">Flowplayer website</a>.</p>